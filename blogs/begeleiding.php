            <h2 class="mt-2 text-2xl font-blod tracking-tight text-gray-900 sm:text-2xl">Met aandacht eten</h2>
            <p class="mt-2">Naast het eten van bepaalde voedingsproducten is het ook belangrijk om de tijd te nemen voor het eten en drinken. Het kan helpen om een plek uit te kiezen zonder afleiding. Het is verder belangrijk om op iedere hap goed te kauwen totdat het vloeibaar is (15 tot 20 keer kauwen). Wanneer je goed kauwt kan het klachten zoals winderigheid en een opgeblazen gevoel verminderen. </p>

            <h2 class="mt-2 text-2xl font-blod tracking-tight text-gray-900 sm:text-2xl">Beweging</h2>
            <p class="mt-2">Regelmatig bewegen kan zorgen voor een goede stoelgang en kan symptomen zoals buikpijn en obstipatie verlichten. Je hebt genoeg lichaamsbeweging wanneer je 150 minuten per week matig of zwaar intensieve inspanning doet, denk aan: wandelen, fietsen, schoonmaken en tuinieren. Bewegen kan ook zorgen voor het verminderen van stress, omdat bewegen zorgt voor het verbeteren van je stemming. </p>
            <p class="mt-4">Elke vorm van lichaamsbeweging kan helpen, maar blijft het wel belangrijk om naar je eigen lichaam te luisteren en activiteiten te kiezen die bij jou als persoon past. Het hebben van een regelmatig bewegingsroutine zou een positief effect kunnen hebben op de PDS-klachten.</p>

            <h2 class="mt-2 text-2xl font-blod tracking-tight text-gray-900 sm:text-2xl">Ontspanning</h2>
            <p class="mt-2">Stress kan PDS-klachten uitlokken of erger maken. Als je klachten door PDS ervaart, zou het wellicht kunnen dat je (langdurig) stress ervaart en dat dit een signaal van je lichaam is. Het beheren van stress en het integreren van ontspanning in je dagelijkse routine kan helpend zijn in het verminderen van de klachten. Je kunt denken aan ontspanningstechnieken, zoals diepe ademhaling, mediteren of yoga, maar ook aan creatieve bezigheden en bewegen. Je kunt kijken naar welke activiteiten je kunt ondernemen waardoor je de gedachten kunt verzetten en het lichaam kan ontspannen.</p>

            <h2 class="mt-2 text-2xl font-blod tracking-tight text-gray-900 sm:text-2xl">Slapen</h2>
            <p class="mt-2">Goed slapen kan een positieve werking hebben op PDS. Goed slapen houdt in dat je minimaal 5 uur ononderbroken slaapt en snel in slaap valt (circa binnen 10 tot 20 minuten). Wanneer je slaapgebrek hebt, gaan de stressniveaus in je lichaam omhoog en heeft dat weer invloed op de darmen. Als je slecht blijft slapen, kan het helpend zijn om te kijken naar de oorzaak. Het handhaven van een regelmatig slaapritme, het creëren van een ontspannende slaapomgeving en praten over spanningen, kunnen helpen om weer goed te gaan slapen. </p>
